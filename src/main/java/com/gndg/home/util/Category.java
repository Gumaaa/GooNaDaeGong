package com.gndg.home.util;

public class Category {
	
	private Long cate_group;
	private String cate_name;
	private String cate_num;
	private String cate_ref;
	
	public Long getCate_group() {
		return cate_group;
	}
	public void setCate_group(Long cate_group) {
		this.cate_group = cate_group;
	}
	public String getCate_name() {
		return cate_name;
	}
	public void setCate_name(String cate_name) {
		this.cate_name = cate_name;
	}
	public String getCate_num() {
		return cate_num;
	}
	public void setCate_num(String cate_num) {
		this.cate_num = cate_num;
	}
	public String getCate_ref() {
		return cate_ref;
	}
	public void setCate_ref(String cate_ref) {
		this.cate_ref = cate_ref;
	}
	
}
