package com.gndg.home.itemFile;

public class ItemFileDTO {
	
	private Long if_fileNum;
	private Long item_num;
	private String if_fileName;
	private String if_oriName;
	
	public Long getIf_fileNum() {
		return if_fileNum;
	}
	public void setIf_fileNum(Long if_fileNum) {
		this.if_fileNum = if_fileNum;
	}
	public Long getItem_num() {
		return item_num;
	}
	public void setItem_num(Long item_num) {
		this.item_num = item_num;
	}
	public String getIf_fileName() {
		return if_fileName;
	}
	public void setIf_fileName(String if_fileName) {
		this.if_fileName = if_fileName;
	}
	public String getIf_oriName() {
		return if_oriName;
	}
	public void setIf_oriName(String if_oriName) {
		this.if_oriName = if_oriName;
	}
	
}
